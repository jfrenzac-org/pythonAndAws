# Write a program that asks the user for a number and classifies it as
# positive, negative, or zero.
numero=int(input("por favor digita un número entero cualquiera: "))
if numero>0:
    print("Este número es un número positivo.")
elif numero<0:
    print("Este número es un número negativo.")

else:
    print("Este número es cero")


